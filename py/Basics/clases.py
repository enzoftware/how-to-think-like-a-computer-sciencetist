class Calculator:

    def __init__(self):
        pass

    @staticmethod
    def addition(x, y):
        print(x+y)

    @staticmethod
    def subtraction(x, y):
        print(x-y)

    @staticmethod
    def multiply(x, y):
        print(x*y)

    @staticmethod
    def division(x, y):
        print(x/y)


Calculator.addition(6, 54)